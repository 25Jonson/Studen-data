
public class Project {
    
    private String name;
    private double invest;

    public Project() {
        name = "";
        invest = 0;
    }

    public Project(String name, double invest) {
        this.name = name;
        this.invest = invest;
    }

    public String getName() {
        return name;
    }

    public double getInvest() {
        return invest;
    }
    
    
    
    
}
